function featuredCard() {
  const t = translations[currentLang];
  const liveBtn = liveUrl
    ? `<a class="proj-link primary" href="${liveUrl}" target="_blank" rel="noopener">${liveIcon}${t["projects.live"]}</a>`
    : "";
  return `
    <article class="proj-card proj-featured">
      <div class="proj-head">${repoIcon}<h3>${portfolioRepo}</h3><span class="proj-tag">${t["projects.featured"]}</span></div>
      <p class="proj-desc">${t["projects.portfolioDesc"]}</p>
      <div class="proj-meta"><span class="lang"><i></i>JavaScript</span></div>
      <div class="proj-actions">
        ${liveBtn}
        <a class="proj-link" href="${portfolioUrl}" target="_blank" rel="noopener">${codeIcon}${t["projects.code"]}</a>
      </div>
    </article>`;
}

function repoCard(r) {
  return `
    <a class="proj-card" href="${r.html_url}" target="_blank" rel="noopener">
      <div class="proj-head">${repoIcon}<h3>${escapeHtml(r.name)}</h3></div>
      <p class="proj-desc">${r.description ? escapeHtml(r.description) : ""}</p>
      <div class="proj-meta">
        ${r.language ? `<span class="lang"><i></i>${escapeHtml(r.language)}</span>` : ""}
        <span>${starIcon}${r.stargazers_count}</span>
      </div>
    </a>`;
}

function renderProjects() {
  const grid = document.getElementById("projectsGrid");
  if (!grid) return;
  if (loadedRepos === null) {
    grid.innerHTML = featuredCard();
    return;
  }
  if (loadedRepos === "error") {
    grid.innerHTML = featuredCard() + `<div class="projects-loading">${translations[currentLang]["projects.error"]}</div>`;
    return;
  }
  grid.innerHTML = featuredCard() + loadedRepos.map(repoCard).join("");
}

async function loadProjects() {
  renderProjects();
  try {
    const res = await fetch(`https://api.github.com/users/${githubUser}/repos?sort=updated&per_page=100`);
    if (!res.ok) throw new Error("request failed");
    const repos = await res.json();
    loadedRepos = repos
      .filter(r => !r.fork && r.name.toLowerCase() !== portfolioRepo)
      .sort((a, b) => b.stargazers_count - a.stargazers_count || new Date(b.updated_at) - new Date(a.updated_at))
      .slice(0, 5);
  } catch (e) {
    loadedRepos = "error";
  }
  renderProjects();
}

function escapeHtml(str) {
  const d = document.createElement("div");
  d.textContent = str;
  return d.innerHTML;
}

function initTheme() {
  const saved = localStorage.getItem("theme");
  const theme = saved || "dark";
  document.documentElement.setAttribute("data-theme", theme);
  document.getElementById("themeToggle").addEventListener("click", () => {
    const next = document.documentElement.getAttribute("data-theme") === "light" ? "dark" : "light";
    document.documentElement.setAttribute("data-theme", next);
    localStorage.setItem("theme", next);
  });
}

function initNav() {
  const nav = document.getElementById("nav");
  const menuBtn = document.getElementById("menuBtn");
  const links = document.querySelector(".nav-links");
  window.addEventListener("scroll", () => {
    nav.classList.toggle("scrolled", window.scrollY > 8);
  });
  menuBtn.addEventListener("click", () => {
    const open = links.classList.toggle("open");
    menuBtn.classList.toggle("open", open);
    menuBtn.setAttribute("aria-expanded", open);
  });
  links.querySelectorAll("a").forEach(a => a.addEventListener("click", () => {
    links.classList.remove("open");
    menuBtn.classList.remove("open");
    menuBtn.setAttribute("aria-expanded", false);
  }));
}

function initReveal() {
  const els = document.querySelectorAll(".section, .hero-inner");
  els.forEach(el => el.classList.add("reveal"));
  const obs = new IntersectionObserver((entries) => {
    entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add("in"); obs.unobserve(e.target); } });
  }, { threshold: 0.12 });
  els.forEach(el => obs.observe(el));
}

document.addEventListener("DOMContentLoaded", () => {
  document.getElementById("year").textContent = new Date().getFullYear();
  initTheme();
  initNav();
  initReveal();
  renderHardSkills();
  document.querySelectorAll(".lang-switch button").forEach(b => {
    b.addEventListener("click", () => applyLang(b.dataset.lang));
  });
  const savedLang = localStorage.getItem("lang");
  const browserLang = (navigator.language || "ca").slice(0, 2);
  applyLang(savedLang || (translations[browserLang] ? browserLang : "ca"));
  loadProjects();
});
