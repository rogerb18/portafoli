function renderProjects() {
  const grid = document.getElementById("projectsGrid");
  if (!grid) return;
  if (loadedRepos === null) {
    grid.innerHTML = featuredCard();
    return;
  }
  if (loadedRepos === "error") {
    grid.innerHTML = featuredCard() + `<div class="projects-loading">${translations[currentLang]["projects.error"]}</div>`;
    return;
  }
  grid.innerHTML = featuredCard() + loadedRepos.map(repoCard).join("");
}

async function loadProjects() {
  renderProjects();
  try {
    const controller = new AbortController();
    const timer = setTimeout(() => controller.abort(), 8000);
    const res = await fetch(`https://api.github.com/users/${githubUser}/repos?sort=updated&per_page=100`, { signal: controller.signal });
    clearTimeout(timer);
    if (!res.ok) throw new Error("request failed");
    const repos = await res.json();
    loadedRepos = repos
      .filter(r => !r.fork && r.name.toLowerCase() !== portfolioRepo)
      .sort((a, b) => b.stargazers_count - a.stargazers_count || new Date(b.updated_at) - new Date(a.updated_at))
      .slice(0, 5);
  } catch (e) {
    loadedRepos = "error";
  }
  renderProjects();
}

function escapeHtml(str) {
  const d = document.createElement("div");
  d.textContent = str;
  return d.innerHTML;
}

function initTheme() {
  const saved = localStorage.getItem("theme");
  const theme = saved || "dark";
  document.documentElement.setAttribute("data-theme", theme);
  document.getElementById("themeToggle").addEventListener("click", () => {
    const next = document.documentElement.getAttribute("data-theme") === "light" ? "dark" : "light";
    document.documentElement.setAttribute("data-theme", next);
    localStorage.setItem("theme", next);
  });
}

function initNav() {
  const nav = document.getElementById("nav");
  const menuBtn = document.getElementById("menuBtn");
  const links = document.querySelector(".nav-links");
  window.addEventListener("scroll", () => {
    nav.classList.toggle("scrolled", window.scrollY > 8);
  });
  menuBtn.addEventListener("click", () => {
    const open = links.classList.toggle("open");
    menuBtn.classList.toggle("open", open);
    menuBtn.setAttribute("aria-expanded", open);
  });
  links.querySelectorAll("a").forEach(a => a.addEventListener("click", () => {
    links.classList.remove("open");
    menuBtn.classList.remove("open");
    menuBtn.setAttribute("aria-expanded", false);
  }));
}

function initReveal() {
  const els = [...document.querySelectorAll(".section, .hero-inner")];
  els.forEach(el => el.classList.add("reveal"));
  const reveal = el => el.classList.add("in");
  if (!("IntersectionObserver" in window)) {
    els.forEach(reveal);
    return;
  }
  const obs = new IntersectionObserver((entries) => {
    entries.forEach(e => { if (e.isIntersecting) { reveal(e.target); obs.unobserve(e.target); } });
  }, { threshold: 0.12 });
  els.forEach(el => obs.observe(el));
  setTimeout(() => els.forEach(reveal), 2500);
}

function initProgress() {
  const bar = document.getElementById("scrollProgress");
  const update = () => {
    const h = document.documentElement;
    const max = h.scrollHeight - h.clientHeight;
    bar.style.width = max > 0 ? `${(h.scrollTop / max) * 100}%` : "0%";
  };
  window.addEventListener("scroll", update, { passive: true });
  update();
}

function initCounters() {
  const nums = [...document.querySelectorAll("[data-count]")];
  const animate = (el) => {
    if (el.dataset.done) return;
    el.dataset.done = "1";
    const target = parseInt(el.dataset.count, 10);
    const suffix = el.dataset.suffix || "";
    const start = performance.now();
    const dur = 1400;
    const step = (now) => {
      const p = Math.min((now - start) / dur, 1);
      const eased = 1 - Math.pow(1 - p, 3);
      el.textContent = Math.round(target * eased) + suffix;
      if (p < 1) requestAnimationFrame(step);
    };
    requestAnimationFrame(step);
  };
  if (!("IntersectionObserver" in window)) {
    nums.forEach(animate);
    return;
  }
  const obs = new IntersectionObserver((entries) => {
    entries.forEach(e => { if (e.isIntersecting) { animate(e.target); obs.unobserve(e.target); } });
  }, { threshold: 0.6 });
  nums.forEach(n => obs.observe(n));
  setTimeout(() => nums.forEach(animate), 2600);
}

function initScrollSpy() {
  const links = [...document.querySelectorAll(".nav-links a")];
  const sections = links
    .map(a => document.querySelector(a.getAttribute("href")))
    .filter(Boolean);
  const obs = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        links.forEach(a => a.classList.toggle("active", a.getAttribute("href") === `#${e.target.id}`));
      }
    });
  }, { rootMargin: "-45% 0px -50% 0px" });
  sections.forEach(s => obs.observe(s));
}

function initSpotlight() {
  if (window.matchMedia("(pointer: coarse)").matches) return;
  document.addEventListener("pointermove", (e) => {
    const card = e.target.closest(".card-glow");
    if (!card) return;
    const rect = card.getBoundingClientRect();
    card.style.setProperty("--mx", `${e.clientX - rect.left}px`);
    card.style.setProperty("--my", `${e.clientY - rect.top}px`);
  }, { passive: true });
}

